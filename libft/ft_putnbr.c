#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	c = '0';
	c = ft_rvminint(&n);
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n / 10 != 0)
		ft_putnbr(n / 10);
	ft_putchar(c + n % 10);
}
